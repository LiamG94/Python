#Arithmetic Operators
#Assign a value to 'a' using Addition
a = 1+2
print(a)
#Assign a value to 'b' using Subtraction
b = 1-2
print(b)
#Assign a value to 'c' using Floor Division
c = 26//4
print(c)
#Assign a value to 'd' using Modulus
d = 10%3
print(d)
#Assign a value to 'e' using Division
e = 9/3
print(e)
#Assign a value to 'f' using Multiplication
f = 2*2
print(f)
#Assign a value to 'g' using Exponentiation 
g = 2**3
print(g)

#Comparison Operators
#Does 'a' Equal 'b'
print(a==b)
#Does 'a' Not Equal 'b'
print(a!=b)
#Is 'a' Greater Than 'b'
print(a>b)
#Is 'a' Less Than 'b'
print(a<b)
#Is 'a' Greater Than or Equal to 'b'
print(a>=b)
#Is 'a' Less Than or Equal to 'b'
print(a<=b)

#Logical and Boolean Operators
#Checks if both statements are 'True'
print(4<3 and 1==1)
#Checks if either statements are 'True'
print(4<3 or 1==1)
#Gives the opposite of the answer
print(not(4<3 or 1==1))
#Checks if a value is in a sequence
print('h' in "John")